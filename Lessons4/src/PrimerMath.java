public class PrimerMath {
    public static void main(String[] args) {
        // Генерация случайных чисел
        System.out.println(Math.random());
        System.out.println((int) (Math.random() * 100));
        System.out.println(Math.ceil(10.00001));
        System.out.println(Math.floor(10.1));
        System.out.println(Math.round(10.3));
        System.out.println(Math.log(10));
        System.out.println(Math.min(10,4));
        System.out.println(Math.max(10,4));
        System.out.println(Math.pow(10,5));     //возведение в степень сначала число потом степень
        System.out.println(Math.sqrt(8));       //Квадратный корень

    }
}
